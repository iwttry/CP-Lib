#include                    <bits/stdc++.h>
using namespace             std;
#define FASTIO              ios::sync_with_stdio(false);cin.tie(0);cout.tie(0);
/*----- ----- ----- ----- ----- ----- ----- ----- ----- -----*/
int p[1000001];
int main(){
    FASTIO;
    int n;
    cin>>n;
    for (int i = 0; i<n; i++){
        cin>>p[i];
    }
    sort(p, p + n);
    int i = 0, j = 0, ans = 0;
    //for (int i = 0; i<n; i++) cout<<p[i]<<" ";
    while (j != n){
        if (p[j + 1] - p[i] > 5){
            i++;
            j = i;
        }
        if (p[j + 1] - p[i] <= 5){
            j++;
            ans = max(ans, j - i);
        }
    }
    cout<<ans;
}