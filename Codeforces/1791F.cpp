int a[maxN], st[4 * maxN], lazy[4 * maxN];

void build(int id, int l, int r){
    if (l == r){
        st[id] = a[l];
        return;
    }
    int m = l + (r - l)/2;
    build(id*2, l, m);
    build(id*2+1, m + 1, r);
}

void fix(int id, int l, int r){
    if (!lazy) return;
    if (l == r){
        for (int i=1; i<=lazy[id]; i++){
            int m = st[id], tmp = 0;
            while (m > 0){
                tmp += m % 10;
                m/=10;
            }
            st[id] = tmp;
        }
    } 
    if (l != r){
        lazy[id * 2] += lazy[id];
        lazy[id * 2 + 1] += lazy[id];
    }
    lazy[id] = 0;
}

void update(int id, int l, int r, int u, int v){
    fix(id, l, r);
    if (l > v || r < u) return;
    if (l >= u && r <= v){
        lazy[id]++;
        fix(id, l, r);
        return;
    }
    int m = l + (r - l)/2;
    update(id * 2, l, m, u, v);
    update(id * 2 + 1, m + 1, r, u, v);
}

int get(int id, int l, int r, int pos){
    fix(id, l, r);
    if (l > pos || r < pos) return 0;
    if (l >= pos && r <= pos){
        return st[id];
    }
    int m = l + (r - l)/2;
    int get1 = get(id * 2, l, m, pos);
    int get2 = get(id * 2 + 1, m + 1, r, pos);
    return get1 + get2;
}

