int LowerDive(std::vector<int> v, int x){
	int l = 0, r = v.size() - 1;
	while (l <= r){
		int m = l + (r - l)/2;
		if (v[m] < x) l = m+1;
		else r = m - 1;
	}
	if (r >= v.size() || r < 0) return -1; return r;
}

